    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.tbMember.getSelectedRow();
        
        String nama = tfNama.getText();
        String kode = tfKode.getText();
        double potongan = tfPotongan.getText() == null ? 1000 : Double.parseDouble(tfPotongan.getText());
        
        this.bisnis.editMember(nama, kode, potongan, selectedRow);
        this.tbMember.setValueAt(this.bisnis.getMember(selectedRow).getNama(), selectedRow, 0);
        this.tbMember.setValueAt(this.bisnis.getMember(selectedRow).getKode(), selectedRow, 1);
        this.tbMember.setValueAt(this.bisnis.getMember(selectedRow).getPotongan(), selectedRow, 2);
    }//GEN-LAST:event_btnEditActionPerformed

    private void tbMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMemberMouseClicked
        // TODO add your handling code here:
        this.row = tbMember.rowAtPoint(evt.getPoint());
        
        if(this.row >= 0) {
            String nama = (String) tbMember.getValueAt(this.row, 0);
            String kode = (String) tbMember.getValueAt(this.row, 1);
            Object potongan = tbMember.getValueAt(this.row, 2);
            
            tfNama.setText(nama);
            tfKode.setText(kode);
            tfPotongan.setText( String.valueOf(potongan));
        }
    }//GEN-LAST:event_tbMemberMouseClicked

