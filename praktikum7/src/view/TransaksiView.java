    private void btnSetorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetorActionPerformed
        // TODO add your handling code here:
        long nominal = (long)formNominal.getValue();
        
        try {
            // get the selected nasabah from the whole bank and perform setor, then modify the bankview table
            this.bank.getNasabah(this.index).getTabungan().simpanUang((int)nominal);
            this.tableModel.setValueAt(this.bank.getNasabah(this.index).getTabungan().getSaldo(), this.index, 2);
            new BankView().displayDialog("Berhasil setor tunai");
            setVisible(false);
        } catch(Exception e) {
            new BankView().displayDialog(e.toString());
        }
    }//GEN-LAST:event_btnSetorActionPerformed

    private void btnTarikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTarikActionPerformed
        // TODO add your handling code here:
        long nominal = (long)formNominal.getValue();
        boolean tarik = this.bank.getNasabah(this.index).getTabungan().ambilUang((int)nominal);
        
        if(tarik) {
            this.tableModel.setValueAt(this.bank.getNasabah(this.index).getTabungan().getSaldo(), this.index, 2);
            new BankView().displayDialog("Berhasil tarik tunai");
            setVisible(false);
        } else {
            new BankView().displayDialog("Saldo ga cukup wkwkwk");
        }
        
    }//GEN-LAST:event_btnTarikActionPerformed

