public class Kalkulator {
    int num1;
    int num2;
    int hasil;
    
    public Kalkulator(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }
    
    public int tambah(int num1, int num2) {
        return hasil = this.num1 + this.num2;
    }
    
    public void printHasil() {
        System.out.println("Hasil = " + hasil);
    }
}
