    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // catch values
        String nama = tfNama.getText();
        String kode = tfKode.getText();
        double potongan = Double.parseDouble(tfPotongan.getText());
        
        
        // call add method
        this.bisnis.tambahMember(nama, kode, potongan);
        
        
            this.model.addRow(new Object[]{this.bisnis.getMember(this.index).getNama(), 
                this.bisnis.getMember(this.index).getKode(), 
                this.bisnis.getMember(this.index).getPotongan()
            });
            this.index++;
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        this.index = tbMember.getSelectedRow();
        
        try{
            this.bisnis.hapusMember(this.index);
            this.model.removeRow(this.index);
            this.index = -1;
            
            this.tfNama.setText("");
            this.tfPotongan.setText("");
            this.tfKode.setText("");
            JOptionPane.showMessageDialog(null, "Berhasil menghapus member");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Mohon pilih member");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

