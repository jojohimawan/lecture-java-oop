public class Produk {
    private String nama;
    private double harga;
    private int stok;
    private int id;
    
    public Produk(String produk, double harga, int stok, int id) {
        this.nama = nama;
        this.harga = harga;
        this.stok = stok;
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public double getHarga() {
        return harga;
    }

    public void setHarga(double harga) {
        this.harga = harga;
    }

    public int getStok() {
        return stok;
    }

    public void setStok(int stok) {
        this.stok = stok;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
